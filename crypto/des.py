def unpack8(src):
    # unpack8(src, dst)
    dst = [0 for _ in range(8)]
    for i in range(8):
        v2 = src[i]
        for j in range(8):
            dst[j] = (v2 >> (7-j)) & 1
    return dst

def pack8(src):
    # pack8(dst, src)
    dst = [0 for _ in range(8)]
    for i in range(8):
        v2 = 0
        for j in range(8):
            v2 = (src[j] + (2 * v2)) & 0xFF
        dst[i] = v2
    return dst